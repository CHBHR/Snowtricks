   public function findOneBySomeField($value): ?Utilisateur
   {
       return $this->createQueryBuilder('utilisateur')
           ->andWhere('utilisateur.email = :val')
           ->setParameter('val', $value)
           ->getQuery()
           ->getOneOrNullResult()
       ;
   }
